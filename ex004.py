n1= input('Digite uma palavra')
print('O tipo primitivo deste valor é',type(n1))
print('Só tem espaço?', n1.isspace())
print('É um número?', n1.isnumeric())
print('É alfabetico?', n1.isalpha())
print('É maiusculo?', n1.isupper())
print('É minusculo?', n1.islower())
print('É capitalizada?', n1.istitle())





